#Program to draw square in Python Turtle
import turtle

t = turtle.Turtle()
t.forward(100) #Forward turtle by 100 units
t.left(90) #Turn turtle by 90 degree
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
